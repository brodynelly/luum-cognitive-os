  continue     → .continue/rules/
  augment      → .augment/rules/
  warp         → WARP.md
  cline        → .clinerules
  zed          → stdout (for settings.json)
EOF
}

count_rules() {
  find "$RULES_DIR" -maxdepth 1 -name '*.md' ! -name 'RULES-COMPACT.md' 2>/dev/null | wc -l | tr -d ' '
}

# Copies individual rule files to a target directory (skipping RULES-COMPACT.md)
copy_rules_to_dir() {
  local target="$1"
  mkdir -p "$target"
  local count=0
  for rule in "$RULES_DIR"/*.md; do
    [ -f "$rule" ] || continue
    local basename
    basename=$(basename "$rule")
    if [ "$basename" = "RULES-COMPACT.md" ]; then
      continue
    fi
    cp "$rule" "$target/$basename"
    count=$((count + 1))
  done
  echo "$count"
}

# Concatenates all rules into a single file with separators
concat_rules_to_file() {
  local target="$1"
  local header="$2"

  {
    echo "$header"
    echo ""
    echo "---"
    echo ""
  } > "$target"

  local count=0
  for rule in "$RULES_DIR"/*.md; do
    [ -f "$rule" ] || continue
    local basename
    basename=$(basename "$rule")
    if [ "$basename" = "RULES-COMPACT.md" ]; then
      continue
    fi
    {
      echo ""
      echo "<!-- Source: rules/$basename -->"
      cat "$rule"
      echo ""
      echo "---"
      echo ""
    } >> "$target"
    count=$((count + 1))
  done
  echo "$count"
}

# Generates a compact rules summary for single-file targets
generate_compact_header() {
  cat <<'HEADEREOF'
# Cognitive OS Rules
# Generated by scripts/ide-bridge.sh
# Full documentation: docs/ide-compatibility.md
#
# These rules define behavioral contracts for AI coding agents.
# Hooks, metrics, and memory require Claude Code or compatible IDE.
# See rules/RULES-COMPACT.md for the full compact index.
HEADEREOF
}

# ── Cursor ────────────────────────────────────────────────────────────

generate_cursor() {
  local target="$PROJECT_ROOT/.cursor/rules"
  local count
  count=$(copy_rules_to_dir "$target")
  echo "Cursor: Generated $count rule files in .cursor/rules/"
  echo "Cursor reads these as AI instructions automatically."
  echo "Note: Register hooks manually in Cursor settings for lifecycle automation."
}

# ── Windsurf ──────────────────────────────────────────────────────────

generate_windsurf() {
  local target="$PROJECT_ROOT/.windsurfrules"
  local header="# Cognitive OS Rules for Windsurf
# Generated by scripts/ide-bridge.sh
# Note: Configure Cascade Hooks manually for lifecycle automation."
  local count
  count=$(concat_rules_to_file "$target" "$header")
  local size
  size=$(wc -c < "$target" | tr -d ' ')
  echo "Windsurf: Generated .windsurfrules with $count rules (${size} bytes)"
  echo "Warning: This is a large single file. Windsurf loads it entirely into context."
}

# ── Aider ─────────────────────────────────────────────────────────────

generate_aider() {
  local target="$PROJECT_ROOT/.aider.conf.yml"
  cat > "$target" << 'AIDEREOF'
# Cognitive OS conventions for Aider
# Generated by scripts/ide-bridge.sh

# Read the compact rules as conventions
read:
  - rules/RULES-COMPACT.md

# Aider configuration
auto-commits: false
lint-cmd: ""

# Convention notes for the AI:
# - Follow the phase-aware behavior in cognitive-os.yaml (project.phase)
# - License policy: BLOCK AGPL, SSPL, BSL. ALLOW MIT, BSD, Apache 2.0, ISC.
# - Every task needs measurable acceptance criteria before starting
# - Classify task complexity (trivial/small/medium/large/critical) before work
# - Admit uncertainties -- "100% confident" is a red flag
# - Never hardcode credentials; use environment variables
# - Check rules/RULES-COMPACT.md for the full behavioral contract
AIDEREOF
  echo "Aider: Generated .aider.conf.yml"
  echo "Aider will read rules/RULES-COMPACT.md as conventions."
}

# ── Gemini CLI ────────────────────────────────────────────────────────

generate_gemini() {
  local target="$PROJECT_ROOT/GEMINI.md"
  if [ -f "$PROJECT_ROOT/CLAUDE.md" ]; then
    cp "$PROJECT_ROOT/CLAUDE.md" "$target"
    echo "Gemini: Generated GEMINI.md (copied from CLAUDE.md)"
  elif [ -f "$COMPACT_RULES" ]; then
    {
      generate_compact_header
      echo ""
      cat "$COMPACT_RULES"
    } > "$target"
    echo "Gemini: Generated GEMINI.md from RULES-COMPACT.md"
  else
    echo "Error: No CLAUDE.md or RULES-COMPACT.md found to generate GEMINI.md" >&2
    exit 1
  fi
  echo "Gemini CLI reads GEMINI.md as global instructions."
}

