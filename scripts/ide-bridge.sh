#!/usr/bin/env bash
# Generate IDE-specific configs from Cognitive OS rules
# Usage:
#   bash scripts/ide-bridge.sh <ide>     # generate config for a specific IDE
#   bash scripts/ide-bridge.sh --list    # list all supported IDEs
#   bash scripts/ide-bridge.sh --help    # show help
#
# Supported IDEs (15):
#   cursor, windsurf, aider, gemini, copilot, copilot-cli, codex,
#   opencode, trae, roo, continue, augment, warp, cline, zed
#
# Note: hooks ONLY work natively with Claude Code. Other IDEs get the RULES layer.
# Tools with full hook support (Gemini CLI, Copilot CLI, Cursor, Windsurf, Kiro)
# may support hooks natively but require manual hook registration.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
RULES_DIR="$PROJECT_ROOT/rules"
COMPACT_RULES="$RULES_DIR/RULES-COMPACT.md"

# ── Helpers ───────────────────────────────────────────────────────────

show_help() {
  cat <<'EOF'
IDE Bridge - Generate IDE-specific configs from Cognitive OS rules

Usage:
  bash scripts/ide-bridge.sh <ide>
  bash scripts/ide-bridge.sh --list
  bash scripts/ide-bridge.sh --help

Supported IDEs (15):
  cursor       .cursor/rules/ directory with individual rule files
  windsurf     .windsurfrules single file with concatenated rules
  aider        .aider.conf.yml with conventions reference
  gemini       GEMINI.md symlinked/generated from CLAUDE.md pattern
  copilot      .github/copilot-instructions.md with project rules
  copilot-cli  .agent.md for GitHub Copilot CLI agent mode
  codex        AGENTS.md for OpenAI Codex CLI
  opencode     .opencode/commands/ directory from rules
  trae         .trae.rules file for Trae (ByteDance)
  roo          .roo/rules-code/ directory for Roo Code
  continue     .continue/rules/ directory for Continue.dev
  augment      .augment/rules/ directory for Augment Code
  warp         WARP.md for Warp terminal
  cline        .clinerules file for Cline
  zed          Prints rules for manual Zed settings.json insertion

What gets generated:
  Rules (behavioral contracts) are converted to each IDE's format.
  Hooks, metrics, memory, and SDD automation require native support
  and may need manual configuration in each IDE.

EOF
}

