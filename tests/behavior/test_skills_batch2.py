class TestSkillCatalogBatch2:

    @pytest.mark.parametrize(
        "skill",
        [s for s in SKILLS_BATCH2 if s != "auto-generated"],
    )
    def test_in_catalog(self, skill: str, skills_dir: Path):
        catalog = skills_dir / "CATALOG.md"
        if not catalog.exists():
            pytest.fail("CATALOG.md not found")
        content = catalog.read_text()
        if skill not in content:
            if skill in EXPERIMENTAL_SKILLS:
                pytest.xfail(f"{skill} not in CATALOG.md (experimental/project-specific skill)")
            else:
                pytest.fail(f"{skill} not in CATALOG.md")


