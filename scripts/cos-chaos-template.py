# ── Helpers ───────────────────────────────────────────────────────────────────

def _setup_project(tmp_path: Path) -> None:
    """Create minimal .cognitive-os/ project structure."""
    (tmp_path / ".cognitive-os" / "runtime").mkdir(parents=True)
    (tmp_path / ".cognitive-os" / "metrics").mkdir(parents=True)
    (tmp_path / ".claude").mkdir(parents=True, exist_ok=True)


def _write_chaos_run(tmp_path: Path, component: str, scenario: str, passed: bool) -> None:
    """Append an exercise record to chaos-runs.jsonl."""
    log = tmp_path / _CHAOS_RUNS_REL
    row = {{
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "event_type": "component.exercised",
        "component": component,
        "scenario": scenario,
        "passed": passed,
        "source": "chaos-test",
    }}
    with log.open("a") as fh:
        fh.write(json.dumps(row) + "\\n")


def _run_component(
    component_path: Path,
    tmp_path: Path,
    stdin_payload: str = "",
    env_extra: dict | None = None,
) -> subprocess.CompletedProcess:
    """Invoke the component as a subprocess with optional stdin and env."""
    base_env = {{
        "PATH": os.environ.get("PATH", "/usr/bin:/bin"),
        "HOME": os.environ.get("HOME", str(tmp_path)),
        "PROJECT_DIR": str(tmp_path),
        "CLAUDE_PROJECT_DIR": str(tmp_path),
        "COGNITIVE_OS_PROJECT_DIR": str(tmp_path),
        # Suppress external service connections
        "VALKEY_DISABLED": "1",
        "REDIS_DISABLED": "1",
    }}
    if env_extra:
        base_env.update(env_extra)

    # Detect interpreter from extension / shebang
    suffix = component_path.suffix.lower()
    if suffix == ".py":
        cmd = ["python3", str(component_path)]
    elif suffix in (".sh", ""):
        cmd = ["bash", str(component_path)]
    else:
        cmd = [str(component_path)]

    return subprocess.run(
        cmd,
        input=stdin_payload,
        capture_output=True,
        text=True,
        timeout=15,
        env=base_env,
        cwd=str(tmp_path),
    )


