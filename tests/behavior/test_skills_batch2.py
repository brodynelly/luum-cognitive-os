EXPERIMENTAL_SKILLS = {"sandbox-sample", "memu-context", "paperclip-dashboard"}

DESCRIPTIVE_SECTION_RE = re.compile(
    r"^## (Purpose|What to Do|What To Do|Overview|When to Use|Goal|Objective|"
    r"What This Skill Does|Description|How It Works|Invocation|Instructions|"
    r"Steps|Procedure|Workflow|Usage|Invoke|Protocol|Trigger|Configuration|"
    r"Execution Protocol|What Gets Validated|Supported Registries|Subcommands)",
    re.MULTILINE,
)


# ---------------------------------------------------------------------------
# Structural tests
# ---------------------------------------------------------------------------


class TestSkillStructureBatch2:

    @pytest.mark.parametrize("skill", SKILLS_BATCH2)
    def test_skill_md_exists(self, skill: str, skills_dir: Path):
        skill_dir = skills_dir / skill
        skill_file = skill_dir / "SKILL.md"
        if skill == "auto-generated":
            # auto-generated might be empty placeholder
            if not skill_file.exists():
                pytest.skip("auto-generated is a placeholder directory")
            return
        assert skill_file.exists(), f"SKILL.md missing for {skill}"

    @pytest.mark.parametrize("skill", [s for s in SKILLS_BATCH2 if s != "auto-generated"])
    def test_has_frontmatter_or_header(self, skill: str, skills_dir: Path):
        skill_file = skills_dir / skill / "SKILL.md"
        if not skill_file.exists():
            pytest.skip(f"SKILL.md missing for {skill}")
        content = skill_file.read_text()
        lines = content.splitlines()
        if not lines:
            pytest.fail(f"Empty SKILL.md for {skill}")
        first_line = lines[0].strip()
        assert first_line.startswith("---") or first_line.startswith("# "), (
            f"missing frontmatter or header for {skill}"
        )

    @pytest.mark.parametrize("skill", [s for s in SKILLS_BATCH2 if s != "auto-generated"])
    def test_has_content_sections(self, skill: str, skills_dir: Path):
        skill_file = skills_dir / skill / "SKILL.md"
        if not skill_file.exists():
            pytest.skip(f"SKILL.md missing for {skill}")
        content = skill_file.read_text()
        assert re.search(r"^## ", content, re.MULTILINE), f"no content sections for {skill}"


