# ── Classifier (inline, mirrors cos-classify-coverage.py logic) ───────────────

_TIER_A_RE = re.compile(
    r"/blocker/|credential|secret|killswitch|destructive|disk[_-]?full"
    r"|content[_-]?policy|rate[_-]?limit|auto[_-]?rollback|auto[_-]?refine"
    r"|auto[_-]?verify|guardrail|license[_-]?guard|concurrent[_-]?write"
    r"|release[_-]?guard|global[_-]?verify|error[_-]?pipeline|error[_-]?learning",
    re.IGNORECASE,
)
_TIER_B_RE = re.compile(
    r"monitor|scheduler|daemon|scanner|heartbeat|reaper|executor"
    r"|registry|capture|session[_-]?init|session[_-]?end",
    re.IGNORECASE,
)
_TIER_D_RE = re.compile(r"skills/[^/]+/SKILL\.md$|rules/[^/]+\.md$|templates/", re.IGNORECASE)


def _infer_tier(component_path: str) -> str:
    # Tier D first: SKILL.md / rules / templates must not be promoted by keyword match
    if _TIER_D_RE.search(component_path):
        return "D"
    if _TIER_A_RE.search(component_path):
        return "A"
    if _TIER_B_RE.search(component_path):
        return "B"
    return "C"


def _safe_name(component_path: str) -> str:
    """Convert component path to a valid Python identifier."""
    name = Path(component_path).stem  # e.g. "secret-detector"
    name = re.sub(r"[^a-zA-Z0-9]", "_", name)
    name = re.sub(r"_+", "_", name).strip("_")
    return name


def _is_script(component_path: str) -> bool:
    """Return True if the component is a Python/shell script (not a hook)."""
    path = component_path.lower()
    return (
        path.startswith("scripts/")
        or path.startswith("lib/")
        or Path(component_path).suffix in (".py",)
        and not path.startswith("hooks/")
    )


def generate_skeleton(
    component_path: str,
    trigger: str | None,
    project_dir: Path,
    output_dir: Path,
) -> Path:
    """Generate a chaos test skeleton and write it to output_dir.

    Returns the path of the written test file.
    """
    tier = _infer_tier(component_path)
    safe = _safe_name(component_path)
    trigger_desc = trigger or f"Standard invocation of {component_path}"

    if _is_script(component_path):
        template = _SCRIPT_TEMPLATE
    else:
        template = _HOOK_TEMPLATE

    content = template.format(
        component_path=component_path,
        tier=tier,
        trigger=trigger_desc,
        safe_name=safe,
    )

    output_path = output_dir / f"test_{safe}_exercised.py"
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content)
    return output_path


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate chaos test skeleton for a dormant component."
    )
    parser.add_argument("component", help="Component path relative to project root (e.g. hooks/secret-detector.sh).")
    parser.add_argument(
        "--trigger",
        default=None,
        help="Description of the trigger condition (used in test docstring).",
    )
    parser.add_argument(
        "--project-dir",
        default=None,
        help="Project root directory (default: auto-detect git root).",
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Output directory for generated test (default: <project-dir>/tests/chaos).",
    )
    args = parser.parse_args()

    # Resolve project directory
    if args.project_dir:
        project_dir = Path(args.project_dir).resolve()
    else:
        cwd = Path.cwd()
        candidate = cwd
        while candidate != candidate.parent:
            if (candidate / ".git").exists():
                project_dir = candidate
                break
            candidate = candidate.parent
        else:
            project_dir = cwd

    output_dir = (
        Path(args.output_dir).resolve()
        if args.output_dir
        else project_dir / "tests" / "chaos"
    )

    out_path = generate_skeleton(args.component, args.trigger, project_dir, output_dir)
    tier = _infer_tier(args.component)
    print(f"Generated Tier {tier} skeleton: {out_path}")


if __name__ == "__main__":
    main()
